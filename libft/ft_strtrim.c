#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*b;
	int		i;

	if (s1 == NULL || set == NULL)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	i = ft_strlen((char *)s1);
	while (i && ft_strchr(set, *(s1 + i)))
		i--;
	b = ft_substr(s1, 0, i + 1);
	return (b);
}
